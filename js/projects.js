        // ТЕКУЩИЙ ПРОЕКТ

        let currentProject = null;

        let currentImageIndex = 0;

        // Контейнер для проектов

        const container = document.getElementById('projectsContainer');

        // Создаем проекты - показываем ПЕРВОЕ изображение из набора

        projectsData.forEach((project, index) => {

            const card = document.createElement('div');

            card.className = 'project-card';

            

            // Показываем первое изображение из массива images

            card.innerHTML = `

                <div class="card-image">

                    <img src="${project.images[0]}" alt="${project.title}" loading="lazy">

                </div>

            `;

            

            // Добавляем обработчик клика - передаем ВЕСЬ ПРОЕКТ

            card.onclick = () => openProjectModal(project);

            

            container.appendChild(card);

        });

        // Функции для модального окна

        function openProjectModal(project) {

            currentProject = project;

            currentImageIndex = 0; // Всегда открываем с первого изображения

            updateModalContent();

            

            const modal = document.getElementById('projectModal');

            modal.classList.add('show');

            document.body.style.overflow = 'hidden';

        }

        function updateModalContent() {

            if (!currentProject) return;

            

            const modalImage = document.getElementById('modalProjectImage');

            const modalTitle = document.getElementById('modalProjectTitle');

            

            modalImage.src = currentProject.images[currentImageIndex];

            modalTitle.textContent = currentProject.title;

        }

        function changeProjectImage(direction) {

            if (!currentProject) return;

            

            currentImageIndex += direction;

            

            // Зацикливаем навигацию ВНУТРИ ТЕКУЩЕГО ПРОЕКТА

            if (currentImageIndex < 0) {

                currentImageIndex = currentProject.images.length - 1;

            } else if (currentImageIndex >= currentProject.images.length) {

                currentImageIndex = 0;

            }

            

            updateModalContent();

        }

        function closeProjectModal() {

            const modal = document.getElementById('projectModal');

            modal.classList.remove('show');

            document.body.style.overflow = 'auto';

            currentProject = null; // Сбрасываем текущий проект

        }

        // Закрытие по клику на фон

        document.getElementById('projectModal').addEventListener('click', function(e) {

            if (e.target === this) {

                closeProjectModal();

            }

        });

        // Навигация с клавиатуры

        document.addEventListener('keydown', function(e) {

            if (!document.getElementById('projectModal').classList.contains('show') || !currentProject) return;

            

            if (e.key === 'Escape') {

                closeProjectModal();

            } else if (e.key === 'ArrowLeft') {

                changeProjectImage(-1);

            } else if (e.key === 'ArrowRight') {

                changeProjectImage(1);

            }

        });

        // Предотвращаем закрытие при клике на стрелки

        document.querySelectorAll('.modal-nav').forEach(btn => {

            btn.addEventListener('click', function(e) {

                e.stopPropagation();

            });

        });
